package labs_examples.fundamentals.labs;

/**
 * Fundamentals Exercise 4: My Second Program
 *
 *      Write the necessary code to display, declare and print each of Java's primitive data types.
 *      Please declare a single variable of each type, give it an appropriate value, then print that variable.
 *
 */

public class Exercise_04 {

    public static void main(String[] args) {

        int i = 1;
        System.out.println("int i is: " + i);

        // write your code below
        int number = 12;
        System.out.println("number is " + number);
        long longNumber = 123456434342L;
        System.out.println("long number is " + longNumber);
        byte byteNumber = 127;
        System.out.println("byte number is " + byteNumber);
        short shortNumber = 23;
        System.out.println("short number is " + shortNumber);
        boolean result = true;
        System.out.println("boolean result is " + result);
        char character = 'p';
        System.out.println("char character is " + character);
        float floatNumber = 1.33F;
        System.out.println("float number is " + floatNumber);
        double doubleNumber = 29.99;
        System.out.println("double number is " + doubleNumber);

    }
}
